package Proekt.menu.homework;

public class AccountInformation {

    private String name;
    private String surname;
    private String age;
    private String phonenumber;
    private String addres;
    private String email;
    private String profession;
    private String role;

    AccountInformation(String name, String surname, int age, String phonenumber, String addres, String email, String profession, String role){
        this.name=name;
        this.surname=surname;
        this.age=age;
        this.phonenumber=phonenumber;
        this.addres=addres;
        this.email=email;
        this.profession=profession;
        this.role=role;



    }

}
